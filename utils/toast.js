import { toast } from "react-toastify";

export const errorToast = (_msg) => {
  toast.error(_msg, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
};

